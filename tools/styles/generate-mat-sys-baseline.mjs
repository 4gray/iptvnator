#!/usr/bin/env node

/**
 * Regenerates tools/styles/mat-sys-baseline.mjs: the per-file counts of
 * legacy `var(--mat-sys-*)` references that predate the guard enforced by
 * tools/styles/check-mat-sys-usage.mjs. These tokens are never emitted by
 * the current Material theme setup, so every reference is a silent no-op.
 * The list should only shrink: migrate a file to `--app-*` tokens, rerun
 * this generator, and commit the result. Never add new entries.
 *
 * Usage: node tools/styles/generate-mat-sys-baseline.mjs
 */

import { writeFileSync } from 'node:fs';
import path from 'node:path';
import process from 'node:process';

import { scanMatSysUsages } from './mat-sys-usage.shared.mjs';

const workspaceRoot = process.cwd();
const usages = scanMatSysUsages(workspaceRoot);

const banner = `// Generated by tools/styles/generate-mat-sys-baseline.mjs — do not edit by hand.
// Files with legacy var(--mat-sys-*) references that predate the guard.
// These tokens are never emitted by the current theme (m3-theme.scss), so
// every reference is a silent no-op. Use the --app-* design tokens instead
// (see docs/architecture/theme-design-tokens.md). This list should only
// shrink: migrate a file, rerun the generator, and commit the result.
`;

const body = usages
    .map(({ file, count }) => `    ['${file}', ${count}],`)
    .join('\n');

writeFileSync(
    path.join(workspaceRoot, 'tools/styles/mat-sys-baseline.mjs'),
    `${banner}export const matSysBaseline = new Map([\n${body}\n]);\n`
);

const total = usages.reduce((sum, { count }) => sum + count, 0);
console.log(`Baselined ${total} usages across ${usages.length} files.`);
